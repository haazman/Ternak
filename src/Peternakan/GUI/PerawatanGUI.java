    public void reset_table(){
        int rowCount = this.tbl_perawatan.getRowCount();
        for(int i = rowCount - 1; i >= 0; i--){
            this.tblmodel.removeRow(i);
        }
    }
    
    public void reset_cmb(){
        this.cmb_petani.removeAllItems();
        this.cmb_hewan.removeAllItems();
        this.cmb_pakan.removeAllItems();
    }
    
    public void load_data() {
        try{
            String select_table_sql = "SELECT *FROM PERAWATAN";
            Statement statement_table = this.conn.createStatement();
            ResultSet result_table = statement_table.executeQuery(select_table_sql);
            
            this.reset_table();
            
            while (result_table.next()){
                String idPerawatan = result_table.getString(1);
                String idPetani = result_table.getString(4);
                String idHewan = result_table.getString(2);
                String idPakan = result_table.getString(3);
                String tanggalPerawatan = result_table.getString(5);
                String dataMenu[] = {idPerawatan,idPetani,idHewan,idPakan,tanggalPerawatan};
                
                this.tblmodel.addRow(dataMenu);
            }
            String select_cmb_pakan_sql = "SELECT *FROM PAKAN";
            Statement statement_cmb_pakan = this.conn.createStatement();
            ResultSet result_cmb_pakan = statement_cmb_pakan.executeQuery(select_cmb_pakan_sql);
            
            String select_cmb_hewan_sql = "SELECT *FROM HEWAN";
            Statement statement_cmb_hewan = this.conn.createStatement();
            ResultSet result_cmb_hewan = statement_cmb_hewan.executeQuery(select_cmb_hewan_sql);
            
            String select_cmb_petani_sql = "SELECT *FROM PETANI";
            Statement statement_cmb_petani = this.conn.createStatement();
            ResultSet result_cmb_petani = statement_cmb_petani.executeQuery(select_cmb_petani_sql);
            
            this.reset_cmb();
            
            while(result_cmb_pakan.next()){
                String idPakan = result_cmb_pakan.getString(1);
                String data = idPakan;
                this.cmb_pakan.addItem(data);
            }
            
            while(result_cmb_petani.next()){
                String idPetani = result_cmb_petani.getString(1);
                String data = idPetani;
                this.cmb_petani.addItem(data);
            }
            
            while(result_cmb_hewan.next()){
                String idHewan = result_cmb_hewan.getString(1);
                String data = idHewan;
                this.cmb_hewan.addItem(data);
            }
            
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }

