    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "INSERT INTO PERAWATAN(ID_H,id_pa,id_pe,tanggal_perawatan) values(?,?,?,?)";
            String id_hewan = this.cmb_hewan.getSelectedItem().toString();
            String id_pakan = this.cmb_pakan.getSelectedItem().toString();
            String id_petani = this.cmb_petani.getSelectedItem().toString();
            String tanggal_perawatan = this.txt_treatDate.getText();
            
            
            System.out.print(id_hewan);
            
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, id_hewan);
            statement.setString(2, id_pakan);
            statement.setString(3, id_petani);
            statement.setString(4, tanggal_perawatan);
            
            int rows = statement.executeUpdate();
            if (rows > 0) {
                this.load_data();
                JOptionPane.showMessageDialog(this, "Perawatan Tanggal " + txt_treatDate.getText() + " Tersimpan");
            }
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_saveActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        try{
            String id = this.txt_id.getText();
            String sql = "DELETE FROM PERAWATAN WHERE ID_PER = ?";
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, id);
            
            int rows = statement.executeUpdate();
            if (rows > 0) {
                this.load_data();
                JOptionPane.showMessageDialog(this, "Menu " + txt_id.getText() + " Terhapus");
            }
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

    private void btn_changeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_changeActionPerformed
        // TODO add your handling code here:
        try{
            String id = this.txt_id.getText();
            
            String tanggal_perawatan = this.txt_treatDate.getText();
            String id_h = this.cmb_hewan.getSelectedItem().toString();
            String id_pa = this.cmb_pakan.getSelectedItem().toString();
            String id_pe = this.cmb_petani.getSelectedItem().toString();
            String sql = "UPDATE PERAWATAN SET TANGGAL_PERAWATAN = ?, id_h = ?, id_pa = ?, id_pe = ? WHERE ID_PER = ?";
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, tanggal_perawatan);
            statement.setString(2, id_h);
            statement.setString(3, id_pa);
            statement.setString(4, id_pe);
            statement.setString(5, id);
            
            int rows = statement.executeUpdate();
            if (rows > 0) {
                this.load_data();
                JOptionPane.showMessageDialog(this, "Menu " + txt_treatDate.getText() + " Terganti");
            }
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_changeActionPerformed

