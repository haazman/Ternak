    private void btn_changeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_changeActionPerformed
        // TODO add your handling code here:
        try{
            String id = this.txt_delete.getText();
            String tipe_pakan = this.text_tipe_pakan.getText();
            String sql = "UPDATE PAKAN SET TIPE_PA = ? WHERE ID_PA = ?";
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, tipe_pakan);
            statement.setString(2, id);
            
            int rows = statement.executeUpdate();
            if (rows > 0) {
                this.load_data();
                JOptionPane.showMessageDialog(this, "Menu " + text_tipe_pakan.getText() + " Terganti");
            }
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_changeActionPerformed

