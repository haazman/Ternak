    public void reset_table(){
        int rowCount = this.tbl_pakan.getRowCount();
        for(int i = rowCount - 1; i >= 0; i--){
            this.tblmodel.removeRow(i);
        }
    }
    
    public void load_data(){
        try{
            String sql = "select * from pakan";
            DBConnection openconn = new DBConnection();
            this.conn = openconn.create_connection();
            Statement statement = this.conn.createStatement();
            ResultSet result = statement.executeQuery(sql);
            
            
            
            this.reset_table();
            
            while (result.next()){
                String idPakan = result.getString(1);
                String tipePakan = result.getString(2);
                String dataMenu[] = {idPakan,tipePakan};
                
                this.tblmodel.addRow(dataMenu);
            }
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }
   
   

